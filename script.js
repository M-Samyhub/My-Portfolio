const root = document.documentElement;
const langToggle = document.getElementById("lang-toggle");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const revealNodes = document.querySelectorAll(
  ".hero-copy > *, .portrait-panel, .section-heading, .feature-card, .skill-card, .timeline-card, .project-hero, .project-card, .contact-copy, .contact-item",
);

function applyLanguage(language) {
  const dictionary = translations[language];

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  root.lang = language;
  root.dir = language === "ar" ? "rtl" : "ltr";
  document.title =
    language === "ar"
      ? "محمد سامي | مطوّر Full Stack .NET"
      : "Mohamed Samy | Full Stack .NET Developer";
  langToggle.textContent = language === "ar" ? "English" : "العربية";
  localStorage.setItem("portfolio-language", language);
}

function setupRevealAnimations() {
  revealNodes.forEach((node, index) => {
    node.setAttribute("data-reveal", "");
    node.style.setProperty("--reveal-delay", `${Math.min(index * 45, 220)}ms`);
  });

  if (!("IntersectionObserver" in window)) {
    revealNodes.forEach((node) => node.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.16,
      rootMargin: "0px 0px -60px 0px",
    },
  );

  revealNodes.forEach((node) => observer.observe(node));
}

langToggle.addEventListener("click", () => {
  applyLanguage(root.lang === "ar" ? "en" : "ar");
});

const savedLanguage = localStorage.getItem("portfolio-language");
applyLanguage(savedLanguage === "en" ? "en" : "ar");
setupRevealAnimations();
